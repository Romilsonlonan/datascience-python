# Criando um programa que recebe dois numeros inteiros e depois apresenta 
# o resultados da soma e da multiplicação, de um numero pelo outro.

numero1 = int(input("informe um número: "))
numero2 = int(input("informe outro numero: "))

resultado_soma = numero1 + numero2
resultado_multiplicacao = numero1 * numero2
print("A soma foi: ", resultado_soma, "e a multiplicação foi", resultado_multiplicacao)
