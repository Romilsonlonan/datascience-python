"""
A instrução Python while é uma das estruturas de repetição disponíveis na linguagem Python, que permite 
executar um bloco de códigos enquanto uma determinada condição for verdadeira. Esse é um comando muito 
utilizado pelas pessoas programadoras no desenvolvimento de aplicações.
"""

nome = input('Insira um nome ')
while nome:
    nome = input('Insira um nome ')
